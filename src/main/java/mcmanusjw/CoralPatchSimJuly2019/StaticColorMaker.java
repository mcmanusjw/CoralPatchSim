  // Specify how many colors you want	
  static Color [] getColorSet(int n)
  {
	  Color [] colors = new Color[n];
	  if (n > 100)
	  {
		  JOptionPane.showMessageDialog(null, "Do not exceed 100 colors in StaticColorMaker!", "", JOptionPane.ERROR_MESSAGE); 
		  return colors;
	  }

	  for (int i = 0; i < n-1; i++)
	  {
		  colors[i] = new Color(values[i][0], values[i][1], values[i][2]);
	  }
	  
	  //Always make last color white to represent damage ellipse     <<<<<<< change this to #2 white
	  colors[n-1] = new Color(255, 255, 255); 
	  
	  return colors;
  }


